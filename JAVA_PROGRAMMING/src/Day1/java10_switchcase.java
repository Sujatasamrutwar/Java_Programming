package Day1;

public class java10_switchcase {
	public static void main(String [] args){
        int variable = 3 ; 
        switch(variable)
        {
            case 1:
                    System.out.println("From case 1");
                    break;
            case 2:
                
                   System.out.println("From case 2");
                   break; 
                
            case 3:
            
                    System.out.println("From case 3");
                    break;
            default:
    
                System.out.println("Out of Range");
        }
    }

}
